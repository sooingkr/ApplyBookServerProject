package com.dayside.dbrs.book.model;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

@JsonInclude(Include.NON_NULL)
public class BookInfo {

	private String GOODS_NM;
	private String GOODS_NO;
	private String AUTH_INFO;
	private String COMPANY2;
	private String ISS_DM;
	
	public String getGOODS_NM() {
		return GOODS_NM;
	}
	public void setGOODS_NM(String gOODS_NM) {
		GOODS_NM = gOODS_NM;
	}
	public String getGOODS_NO() {
		return GOODS_NO;
	}
	public void setGOODS_NO(String gOODS_NO) {
		GOODS_NO = gOODS_NO;
	}
	public String getAUTH_INFO() {
		return AUTH_INFO;
	}
	public void setAUTH_INFO(String aUTH_INFO) {
		AUTH_INFO = aUTH_INFO;
	}
	public String getCOMPANY2() {
		return COMPANY2;
	}
	public void setCOMPANY2(String cOMPANY2) {
		COMPANY2 = cOMPANY2;
	}
	public String getISS_DM() {
		return ISS_DM;
	}
	public void setISS_DM(String iSS_DM) {
		ISS_DM = iSS_DM;
	}

	@Override
	public String toString() {
		return "BookInfo [GOODS_NM=" + GOODS_NM + ", GOODS_NO=" + GOODS_NO + ", AUTH_INFO=" + AUTH_INFO + ", COMPANY2="
				+ COMPANY2 + ", ISS_DM=" + ISS_DM + "]";
	}
	
}

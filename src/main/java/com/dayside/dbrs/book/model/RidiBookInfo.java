package com.dayside.dbrs.book.model;

public class RidiBookInfo extends Book {

	private String imageUrl;
	private String author;
	private String  publisher;
	private String  subTitle;
	private String count_num;
	private String entire_purchase;
	private String description;
	private String bookTypeBadge;
	private String  seriesComplete;
	private String ridiPrice;
	
	public String getRidiPrice() {
		return ridiPrice;
	}
	public void setRidiPrice(String ridiPrice) {
		this.ridiPrice = ridiPrice;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	public String getSubTitle() {
		return subTitle;
	}
	public void setSubTitle(String subTitle) {
		this.subTitle = subTitle;
	}
	public String getCount_num() {
		return count_num;
	}
	public void setCount_num(String count_num) {
		this.count_num = count_num;
	}
	public String getEntire_purchase() {
		return entire_purchase;
	}
	public void setEntire_purchase(String entire_purchase) {
		this.entire_purchase = entire_purchase;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getBookTypeBadge() {
		return bookTypeBadge;
	}
	public void setBookTypeBadge(String bookTypeBadge) {
		this.bookTypeBadge = bookTypeBadge;
	}
	public String getSeriesComplete() {
		return seriesComplete;
	}
	public void setSeriesComplete(String seriesComplete) {
		this.seriesComplete = seriesComplete;
	}
	


	
	
}
